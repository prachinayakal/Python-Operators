# 2. Comparision
#Comparision operator is used to compare two values and write true or false
#comparision operator contains ==(equal to), !=(not equal to), >(greater than), 
# <(less than), >=(greater than or equal to), <=(less than or equal to)

# 2.1 ==(equal to)
print(5==5)

# 2.2 !=(not equal to)
print(5!=6)

# 2.3 >(greater than)
print(8>5)
 
# 2.4 <(less than)
print(4<45)

# 2.5 >=(greater than or equal to)
print(15>=10)

# 2.6 <=(less than or equal to)
print(10<=10)



